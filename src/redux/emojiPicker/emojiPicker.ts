const emojiPicker = (state = initialState, action) => {
  switch (action.type) {
    case SET_SHOWING_EMOJIES:
      return {
        ...state,
        isShowEmojies: action.payload,
      };
    case UPDATE_NEW_MESSAGE_BODY:
      return {
        ...state,
        newMessageBody: action.payload,
      };
    case SEND_NEW_MESSAGE:
      return {
        ...state,
        messages: [...state.messages, action.payload],
      };
    case ADD_EMOJI_IN_RECENT:
      return {
        ...state,
        recentEmojies: [
          ...new Set([...state.recentEmojies, action.payload]),
        ].splice(-maxQuantityOfRecentEmojies),
      };
    default:
      return state;
  }
};

